package com.nhnacademy.node;

public class Message {

    int id;
    String name;
    String message;
    

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getMessage() {
        return message;
    }



    public void setName(String name) {
        this.name = name;
    }


}
